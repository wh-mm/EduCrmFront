<template>
  <basic-container>
    <avue-crud :option="option"
               :table-loading="loading"
               :data="data"
               :page.sync="page"
               :permission="permissionList"
               :before-open="beforeOpen"
               v-model="form"
               ref="crud"
               @row-update="rowUpdate"
               @row-save="rowSave"
               @row-del="rowDel"
               @search-change="searchChange"
               @search-reset="searchReset"
               @selection-change="selectionChange"
               @current-change="currentChange"
               @size-change="sizeChange"
               @refresh-change="refreshChange"
               @on-load="onLoad">
      <template slot="menuLeft">
        <el-button type="danger"
                   size="small"
                   icon="el-icon-delete"
                   plain
                   v-if="permission.outwarehouseorderdetail_delete"
                   @click="handleDelete">删 除
        </el-button>
      </template>
    </avue-crud>
  </basic-container>
</template>

<script>
  import {getList, getDetail, add, update, remove} from "@/api/warehouse/outwarehouseorderdetail";
  import {mapGetters} from "vuex";

  export default {
    data() {
      return {
        form: {},
        query: {},
        loading: true,
        page: {
          pageSize: 10,
          currentPage: 1,
          total: 0
        },
        selectionList: [],
        option: {
          height:'auto',
          calcHeight: 30,
          tip: false,
          searchShow: true,
          searchMenuSpan: 6,
          border: true,
          index: true,
          viewBtn: true,
          selection: true,
          dialogClickModal: false,
          column: [
            {
              label: "入库id",
              prop: "outputwarehouseId",
              rules: [{
                required: true,
                message: "请输入入库id",
                trigger: "blur"
              }]
            },
            {
              label: "仓库id",
              prop: "warehouseId",
              rules: [{
                required: true,
                message: "请输入仓库id",
                trigger: "blur"
              }]
            },
            {
              label: "区id",
              prop: "storageRegionId",
              rules: [{
                required: true,
                message: "请输入区id",
                trigger: "blur"
              }]
            },
            {
              label: "储位Id",
              prop: "storageId",
              rules: [{
                required: true,
                message: "请输入储位Id",
                trigger: "blur"
              }]
            },
            {
              label: "批号",
              prop: "batchNumber",
              rules: [{
                required: true,
                message: "请输入批号",
                trigger: "blur"
              }]
            },
            {
              label: "商品id",
              prop: "goodsId",
              rules: [{
                required: true,
                message: "请输入商品id",
                trigger: "blur"
              }]
            },
            {
              label: "数量",
              prop: "goodsQuantity",
              rules: [{
                required: true,
                message: "请输入数量",
                trigger: "blur"
              }]
            },
            {
              label: "复核数量",
              prop: "recheckGoodsQuantity",
              rules: [{
                required: true,
                message: "请输入复核数量",
                trigger: "blur"
              }]
            },
            {
              label: "单位",
              prop: "unit",
              rules: [{
                required: true,
                message: "请输入单位",
                trigger: "blur"
              }]
            },
            {
              label: "规格",
              prop: "specification",
              rules: [{
                required: true,
                message: "请输入规格",
                trigger: "blur"
              }]
            },
            {
              label: "供应商",
              prop: "supplierName",
              rules: [{
                required: true,
                message: "请输入供应商",
                trigger: "blur"
              }]
            },
            {
              label: "生产日期",
              prop: "dateOfManufacture",
              rules: [{
                required: true,
                message: "请输入生产日期",
                trigger: "blur"
              }]
            },
            {
              label: "有效期至",
              prop: "periodOfValidity",
              rules: [{
                required: true,
